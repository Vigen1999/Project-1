#include "libft.h"

int		ft_atoi(const char *str)
{
	int s;
	int v;

	s = 1;
	v = 0;
	while (*str == '\n' || *str == '\t' || *str == '\v'
			|| *str == '\r' || *str == '\f' || *str == ' ')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			s *= -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		v = (v * 10) + (*str - 48);
		str++;
	}
	return (s * v);
}
